// import toast
import { toast } from "react-toastify";

const toastSuccess = (str) => {
	toast.success(str, {
		position: "top-center",
		autoClose: 5000,
		hideProgressBar: false,
		closeOnClick: true,
		pauseOnHover: true,
		draggable: true,
		progress: undefined,
		theme: "dark",
	});
};

